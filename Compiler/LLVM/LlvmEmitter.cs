	private static string LlvmType(CirType t) => t switch {
		CirType.Void => "void",
		CirType.Any => "ptr",
		CirType.Ptr => "ptr",
		CirType.Nullable n => LlvmType(n.Inner),
		// Slice: a two-word value carrying a data pointer and an i64 length. All arrays
		// (`T[]`, regardless of element type) use the same in-memory shape; the element
		// type lives at the CIR level and is consulted at the GEP / load site.
		CirType.Array => "{ ptr, i64 }",
		CirType.Tuple => "ptr",
		CirType.Generic => "ptr",
		CirType.Named n => n.FullyQualifiedName switch {
			// Canonical primitive types (also produced by SemanticAnalyzer aliasing).
			"i8" or "u8" => "i8",
			"i16" or "u16" => "i16",
			"i32" or "u32" => "i32",
			"i64" or "u64" => "i64",
			"f32" => "float",
			"f64" => "double",
			// Aliases — kept for back-compat with code that hasn't been canonicalized.
			"int" => "i32",
			"uint" => "i32",
			"unsigned" => "i32",
			"long" => "i64",
			"short" => "i16",
			"float" => "float",
			"double" => "double",
			"real" => "double",
			// Other primitives.
			"string" => "ptr",
			"byte" => "i8",
			"sbyte" => "i8",
			"char" => "i8",
			"bool" => "i1",
			"bit" => "i1",
			"void" => "void",
			"any" => "ptr",
			_ => "ptr"
		},
		_ => "ptr"
	};

	private static string DefaultValue(CirType t) => t switch {
		CirType.Named n when n.FullyQualifiedName is "i8" or "i16" or "i32" or "i64" or "u8" or "u16" or "u32" or "u64" or "int" or "uint" or "unsigned" or "long" or "short" or "byte" or "sbyte" or "char" or "bit" => "0",
		CirType.Named n when n.FullyQualifiedName is "bool" => "0",
		CirType.Named n when n.FullyQualifiedName is "f32" or "f64" or "float" or "double" or "real" => "0.0",
		_ => "null"
	};

	private static string LlvmBinOp(CirBinOp op, string ty) {
		var isFloat = ty is "float" or "double";
		return op switch {
			CirBinOp.Add => isFloat ? "fadd" : "add",
			CirBinOp.Sub => isFloat ? "fsub" : "sub",
			CirBinOp.Mul => isFloat ? "fmul" : "mul",
			CirBinOp.Div => isFloat ? "fdiv" : "sdiv",
			CirBinOp.Rem => isFloat ? "frem" : "srem",
			CirBinOp.BitAnd or CirBinOp.And => "and",
			CirBinOp.BitOr or CirBinOp.Or => "or",
			CirBinOp.Shl => "shl",
			CirBinOp.Shr => "ashr",
			CirBinOp.Eq => isFloat ? "fcmp oeq" : "icmp eq",
			CirBinOp.NotEq => isFloat ? "fcmp one" : "icmp ne",
			CirBinOp.Lt => isFloat ? "fcmp olt" : "icmp slt",
			CirBinOp.LtEq => isFloat ? "fcmp ole" : "icmp sle",
			CirBinOp.Gt => isFloat ? "fcmp ogt" : "icmp sgt",
			CirBinOp.GtEq => isFloat ? "fcmp oge" : "icmp sge",
			_ => "add"
		};
	}

	private static CirBinOp AssignToBinOp(CirAssignOp op) => op switch {
		CirAssignOp.AddAssign => CirBinOp.Add,
		CirAssignOp.SubAssign => CirBinOp.Sub,
		CirAssignOp.MulAssign => CirBinOp.Mul,
		CirAssignOp.DivAssign => CirBinOp.Div,
		CirAssignOp.RemAssign => CirBinOp.Rem,
		CirAssignOp.AndAssign => CirBinOp.BitAnd,
		CirAssignOp.OrAssign => CirBinOp.BitOr,
		CirAssignOp.PowAssign => CirBinOp.Pow,
		_ => CirBinOp.Add
	};

