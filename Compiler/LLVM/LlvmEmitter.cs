	// Expressions
	// -------------------------------------------------------------------------

	// Returns the SSA name (e.g. "%_t3") or literal (e.g. "42") representing the value.
	private string EmitExpr(CirExpr expr) {
		switch (expr) {
			case CirExpr.IntLit i: return i.Value;
			case CirExpr.FloatLit f: return f.Value;
			case CirExpr.BoolLit b: return b.Value ? "1" : "0";
			case CirExpr.CharLit c: return ((int)c.Value).ToString(CultureInfo.InvariantCulture);
			case CirExpr.StrLit s: return InternString(s.Value);
			case CirExpr.NullLit: return "null";

			case CirExpr.Local l: return EmitLocalLoad(l.Name);
			case CirExpr.ThisPtr: return EmitLocalLoad("this");

			case CirExpr.FieldAccess fa: return EmitFieldLoad(fa);
			case CirExpr.StaticFieldRef sr: return EmitStaticFieldLoad(sr);
			case CirExpr.EnumCaseRef ec: return "@" + MangleEnumCaseGlobal(ec.EnumFqn, ec.CaseName);
			case CirExpr.ArrayLit al: return EmitArrayLit(al);
			case CirExpr.Index ix: return EmitIndexLoad(ix);
			case CirExpr.ArrayLength al: return EmitArrayLength(al);
			case CirExpr.NewArray na: return EmitNewArray(na);
			case CirExpr.Subslice ss: return EmitSubslice(ss);
			case CirExpr.Call c: return EmitCall(c);
			case CirExpr.Alloc a: return EmitAlloc(a);
			case CirExpr.VtableRef v: return $"@{MangleVtableGlobal(v.ClassFqn)}";
			case CirExpr.VirtualCall vc: return EmitVirtualCall(vc);
			case CirExpr.NullCoalesce nc: return EmitNullCoalesce(nc);
			case CirExpr.Ternary tn: return EmitTernary(tn);
			case CirExpr.Downcast dc: return EmitDowncast(dc);
			case CirExpr.TypeCheck tc: return EmitTypeCheck(tc);

			case CirExpr.Binary b: return EmitBinary(b);
			case CirExpr.Unary u: return EmitUnary(u);
			case CirExpr.Cast cast: return EmitCast(cast);
		}

		LlvmError.UnsupportedExpression.WithMessage($"{expr.GetType().Name} is not yet lowerable to LLVM IR").Render();
		return "undef";
	}

	private string EmitLocalLoad(string name) {
		if (!_localAddrMap.TryGetValue(name, out var addr) || !_localTypeMap.TryGetValue(name, out var ty)) {
			LlvmError.UnsupportedExpression.WithMessage($"reference to unknown local '{name}'").Render();
			return "undef";
		}

		var t = FreshTemp();
		_bodyLines.Add($"  {t} = load {LlvmType(ty)}, ptr {addr}");
		return t;
	}

	private string EmitFieldLoad(CirExpr.FieldAccess fa) {
		var (gep, fieldType) = EmitFieldGep(fa);
		var t = FreshTemp();
		_bodyLines.Add($"  {t} = load {LlvmType(fieldType)}, ptr {gep}");
		return t;
	}

	// Emit `[a, b, c]` as a heap-allocated slice. Allocates a `[N x T]` buffer via
	// calloc (already-declared in PreScan), stores each element, then constructs the
	// slice value `{ ptr data, i64 N }` to return. Caller's `let` binding holds the
	// slice as a local; the underlying data lives until the slice is explicitly deleted.
	private string EmitArrayLit(CirExpr.ArrayLit lit) {
		var eltTy = LlvmType(lit.ElementType);
		var n = lit.Elements.Count;

		// Compute sizeof(T) via the GEP-null trick: the address of the second element of
		// a zero-based array gives the size of one element.
		var sizePtr = FreshTemp();
		_bodyLines.Add($"  {sizePtr} = getelementptr {eltTy}, ptr null, i64 1");
		var sizeI64 = FreshTemp();
		_bodyLines.Add($"  {sizeI64} = ptrtoint ptr {sizePtr} to i64");

		// Allocate the backing buffer. calloc zero-fills, which matches the rest of the
		// codebase's "new -> zero-init" convention and means uninitialized slots are 0/null.
		var data = FreshTemp();
		_bodyLines.Add($"  {data} = call ptr @calloc(i64 {n}, i64 {sizeI64})");

		// Store each element at its index.
		for (var i = 0; i < lit.Elements.Count; i++) {
			var v = EmitExpr(lit.Elements[i]);
			var coerced = CoerceTo(v, LlvmTypeOf(lit.Elements[i]), eltTy);
			var slot = FreshTemp();
			_bodyLines.Add($"  {slot} = getelementptr {eltTy}, ptr {data}, i64 {i}");
			_bodyLines.Add($"  store {eltTy} {coerced}, ptr {slot}");
		}

		// Build the slice value: `{ ptr data, i64 N }`.
		var sliceWithData = FreshTemp();
		_bodyLines.Add($"  {sliceWithData} = insertvalue {{ ptr, i64 }} undef, ptr {data}, 0");
		var slice = FreshTemp();
		_bodyLines.Add($"  {slice} = insertvalue {{ ptr, i64 }} {sliceWithData}, i64 {n}, 1");
		return slice;
	}

	// `new T[a]` / `new T[a][b]` / … — heap-allocate a (possibly nested) array. Single-
	// dimensional is a single calloc + slice build. Multi-dimensional allocates the
	// outer buffer, then loops storing a fresh inner allocation into each slot.
	private string EmitNewArray(CirExpr.NewArray na) {
		// Pre-evaluate every dimension's size so we don't re-run side effects across
		// loop iterations. Each ends up as an i64 SSA value.
		var sizes = na.Sizes.Select(s => CoerceTo(EmitExpr(s), LlvmTypeOf(s), "i64")).ToList();
		return EmitNewArrayDim(na.ElementType, sizes, 0);
	}

	// Recursive helper: allocate dimension `depth`'s buffer, and if there are inner
	// dimensions left, run a loop that fills each outer slot with a fresh allocation
	// of the next level.
	private string EmitNewArrayDim(CirType leafElementType, List<string> sizes, int depth) {
		var remaining = sizes.Count - depth;
		var size = sizes[depth];

		// Element type at this level: leaf when this is the deepest dim, slice-of-slices
		// (or slice-of-leaf) wrapped as `{ ptr, i64 }` for outer dims.
		var isLeaf = remaining == 1;
		var thisEltTy = isLeaf ? LlvmType(leafElementType) : "{ ptr, i64 }";

		// sizeof(thisEltTy) via GEP-null trick.
		var sizePtr = FreshTemp();
		_bodyLines.Add($"  {sizePtr} = getelementptr {thisEltTy}, ptr null, i64 1");
		var eltBytes = FreshTemp();
		_bodyLines.Add($"  {eltBytes} = ptrtoint ptr {sizePtr} to i64");

		var data = FreshTemp();
		_bodyLines.Add($"  {data} = call ptr @calloc(i64 {size}, i64 {eltBytes})");

		if (!isLeaf) {
			// Fill each slot with a fresh allocation of the next inner dimension.
			var iAddr = FreshLabel("newarr_i") + ".addr";
			_allocaLines.Add($"  %{iAddr} = alloca i64, align 8");
			_bodyLines.Add($"  store i64 0, ptr %{iAddr}");

			var condLabel = FreshLabel("newarr_cond");
			var bodyLabel = FreshLabel("newarr_body");
			var endLabel = FreshLabel("newarr_end");

			_bodyLines.Add($"  br label %{condLabel}");
			_bodyLines.Add($"{condLabel}:");
			var iVal = FreshTemp();
			_bodyLines.Add($"  {iVal} = load i64, ptr %{iAddr}");
			var cmp = FreshTemp();
			_bodyLines.Add($"  {cmp} = icmp slt i64 {iVal}, {size}");
			_bodyLines.Add($"  br i1 {cmp}, label %{bodyLabel}, label %{endLabel}");

			_bodyLines.Add($"{bodyLabel}:");
			_blockTerminated = false;
			var innerSlice = EmitNewArrayDim(leafElementType, sizes, depth + 1);
			var slot = FreshTemp();
			_bodyLines.Add($"  {slot} = getelementptr {{ ptr, i64 }}, ptr {data}, i64 {iVal}");
			_bodyLines.Add($"  store {{ ptr, i64 }} {innerSlice}, ptr {slot}");
			var iNext = FreshTemp();
			_bodyLines.Add($"  {iNext} = add i64 {iVal}, 1");
			_bodyLines.Add($"  store i64 {iNext}, ptr %{iAddr}");
			_bodyLines.Add($"  br label %{condLabel}");

			_bodyLines.Add($"{endLabel}:");
			_blockTerminated = false;
		}

		var sliceWithData = FreshTemp();
		_bodyLines.Add($"  {sliceWithData} = insertvalue {{ ptr, i64 }} undef, ptr {data}, 0");
		var slice = FreshTemp();
		_bodyLines.Add($"  {slice} = insertvalue {{ ptr, i64 }} {sliceWithData}, i64 {size}, 1");
		return slice;
	}

	// `arr[lo..hi]` — produce a fresh slice over `target`'s buffer starting at `lo`,
	// length `hi - lo`. Bounds: `0 <= lo <= hi <= len`. Reuses `@__cloth_panic_bounds`
	// for the failure path (idx = lo on panic, so the message is at least informative).
	// The sub-slice borrows — the user must keep the parent live for its lifetime.
	private string EmitSubslice(CirExpr.Subslice ss) {
		_needsBoundsPanic = true;

		var slice = EmitExpr(ss.Target);
		var data = FreshTemp();
		_bodyLines.Add($"  {data} = extractvalue {{ ptr, i64 }} {slice}, 0");
		var len = FreshTemp();
		_bodyLines.Add($"  {len} = extractvalue {{ ptr, i64 }} {slice}, 1");

		var lo = CoerceTo(EmitExpr(ss.Lo), LlvmTypeOf(ss.Lo), "i64");
		var hi = CoerceTo(EmitExpr(ss.Hi), LlvmTypeOf(ss.Hi), "i64");

		// Bounds: lo < 0, hi > len, lo > hi — any one is a panic.
		var negLo = FreshTemp();
		_bodyLines.Add($"  {negLo} = icmp slt i64 {lo}, 0");
		var hiOver = FreshTemp();
		_bodyLines.Add($"  {hiOver} = icmp sgt i64 {hi}, {len}");
		var loGtHi = FreshTemp();
		_bodyLines.Add($"  {loGtHi} = icmp sgt i64 {lo}, {hi}");
		var bad1 = FreshTemp();
		_bodyLines.Add($"  {bad1} = or i1 {negLo}, {hiOver}");
		var bad = FreshTemp();
		_bodyLines.Add($"  {bad} = or i1 {bad1}, {loGtHi}");
		var panicLabel = FreshLabel("subslice_panic");
		var okLabel = FreshLabel("subslice_ok");
		_bodyLines.Add($"  br i1 {bad}, label %{panicLabel}, label %{okLabel}");
		_bodyLines.Add($"{panicLabel}:");
		_bodyLines.Add($"  call void @__cloth_panic_bounds(i64 {lo}, i64 {len})");
		_bodyLines.Add($"  unreachable");
		_bodyLines.Add($"{okLabel}:");
		_blockTerminated = false;

		var eltTy = LlvmType(ss.ElementType);
		var newData = FreshTemp();
		_bodyLines.Add($"  {newData} = getelementptr {eltTy}, ptr {data}, i64 {lo}");
		var newLen = FreshTemp();
		_bodyLines.Add($"  {newLen} = sub i64 {hi}, {lo}");
		var sliceWithData = FreshTemp();
		_bodyLines.Add($"  {sliceWithData} = insertvalue {{ ptr, i64 }} undef, ptr {newData}, 0");
		var subslice = FreshTemp();
		_bodyLines.Add($"  {subslice} = insertvalue {{ ptr, i64 }} {sliceWithData}, i64 {newLen}, 1");
		return subslice;
	}

	// `arr::LENGTH` — extract the length field (offset 1) of the slice value.
	private string EmitArrayLength(CirExpr.ArrayLength al) {
		var slice = EmitExpr(al.Target);
		var t = FreshTemp();
		_bodyLines.Add($"  {t} = extractvalue {{ ptr, i64 }} {slice}, 1");
		return t;
	}

	// Load an element from a slice: `arr[i]`. Extracts the data pointer and length from
	// the slice, validates the index against `0 <= i < length`, then GEPs to the i-th
	// element and loads. An out-of-bounds index calls `@__cloth_panic_bounds` (lazy-
	// emitted in `Build`), which prints a diagnostic and aborts.
	private string EmitIndexLoad(CirExpr.Index ix) {
		_needsBoundsPanic = true;

		var slice = EmitExpr(ix.Target);
		var data = FreshTemp();
		_bodyLines.Add($"  {data} = extractvalue {{ ptr, i64 }} {slice}, 0");
		var len = FreshTemp();
		_bodyLines.Add($"  {len} = extractvalue {{ ptr, i64 }} {slice}, 1");

		var idx = EmitExpr(ix.Idx);
		var idxTy = LlvmTypeOf(ix.Idx);
		var idx64 = CoerceTo(idx, idxTy, "i64");

		// Bounds check: signed comparisons cover both negative indices and >= length.
		var neg = FreshTemp();
		_bodyLines.Add($"  {neg} = icmp slt i64 {idx64}, 0");
		var oob = FreshTemp();
		_bodyLines.Add($"  {oob} = icmp sge i64 {idx64}, {len}");
		var bad = FreshTemp();
		_bodyLines.Add($"  {bad} = or i1 {neg}, {oob}");
		var panicLabel = FreshLabel("bounds_panic");
		var okLabel = FreshLabel("bounds_ok");
		_bodyLines.Add($"  br i1 {bad}, label %{panicLabel}, label %{okLabel}");
		_bodyLines.Add($"{panicLabel}:");
		_bodyLines.Add($"  call void @__cloth_panic_bounds(i64 {idx64}, i64 {len})");
		_bodyLines.Add($"  unreachable");
		_bodyLines.Add($"{okLabel}:");
		_blockTerminated = false;

		var eltTy = LlvmElementTypeOf(ix.Target);
		var slot = FreshTemp();
		_bodyLines.Add($"  {slot} = getelementptr {eltTy}, ptr {data}, i64 {idx64}");
		var t = FreshTemp();
		_bodyLines.Add($"  {t} = load {eltTy}, ptr {slot}");
		return t;
	}

	// Return the LLVM element type for an array-typed expression. Falls back to `ptr`
	// when the expression isn't recognized as array-producing (defensive — should not
	// happen in valid code).
	private string LlvmElementTypeOf(CirExpr expr) {
		var arr = GetCirArrayResultType(expr);
		return arr != null ? LlvmType(arr.Element) : "ptr";
	}

	// Recover the array type produced by an expression as a `CirType.Array`. Returns null
	// when the expression isn't array-typed (e.g. a scalar `arr[i]` on a 1-d slice, or a
	// call whose return type is non-array). Used by both `LlvmElementTypeOf` and
	// `TryGetArrayElementCirType` so the two views stay in sync.
	private CirType.Array? GetCirArrayResultType(CirExpr expr) {
		switch (expr) {
			case CirExpr.ArrayLit al:
				return new CirType.Array(al.ElementType);
			case CirExpr.NewArray na: {
				// Multi-dim `new T[a][b][c]`: the outer result type is `T[][][]`. Wrap the
				// leaf once per declared dimension.
				CirType t = na.ElementType;
				for (var i = 0; i < na.Sizes.Count; i++) t = new CirType.Array(t);
				return (CirType.Array) t;
			}
			case CirExpr.Subslice ss:
				return new CirType.Array(ss.ElementType);
			case CirExpr.Local l when _localTypeMap.TryGetValue(l.Name, out var ty) && ty is CirType.Array arr:
				return arr;
			case CirExpr.Call c:
				var fn = _module.Functions.FirstOrDefault(f => f.MangledName == c.MangledName);
				return fn?.ReturnType as CirType.Array;
			case CirExpr.Index ix:
				// `target[i]` produces target-element. If target-element is itself an array
				// (i.e. target was 2+ dim), the Index result is that inner array.
				var outerArr = GetCirArrayResultType(ix.Target);
				return outerArr?.Element as CirType.Array;
			case CirExpr.FieldAccess fa:
				var fqn = ResolveTargetFqn(fa.Target);
				if (fqn != null && _classByFqn.ContainsKey(fqn)) {
					var f = GetFlattenedFields(fqn).FirstOrDefault(x => x.Name == fa.FieldName);
					return f?.Type as CirType.Array;
				}
				return null;
		}
		return null;
	}

	// Load a class-level static / const field by emitting a `load` from the corresponding
	// LLVM global. The global's symbol is `@<mangled-class>_<field>` (matching
	// `EmitStaticFieldGlobals` and the cross-project `external` declaration shape).
	private string EmitStaticFieldLoad(CirExpr.StaticFieldRef sr) {
		var globalName = MangleStaticGlobal(sr.ClassFqn, sr.Name);
		var llvmTy = LlvmType(sr.Type);
		var t = FreshTemp();
		_bodyLines.Add($"  {t} = load {llvmTy}, ptr @{globalName}");
		return t;
	}

	private (string gep, CirType fieldType) EmitFieldGep(CirExpr.FieldAccess fa) {
		var targetVal = EmitExpr(fa.Target);
		var fqn = ResolveTargetFqn(fa.Target);
		if (fqn == null) {
			LlvmError.FieldNotFound.WithMessage($"cannot resolve owning type of field '{fa.FieldName}'").Render();
			return ("undef", new CirType.Any());
		}

		// Enum field layout: { i32 ordinal, ptr name, <params...> }. The two built-in
		// slots are referenced by the synthesized `getOrdinal()` and `name()` getters
		// using their `__ordinal__` / `__name__` reserved field names.
		if (_enumByFqn.TryGetValue(fqn, out var enumDecl)) {
			var (idx, fieldTy) = ResolveEnumFieldIndex(enumDecl, fa.FieldName);
			if (idx < 0) {
				LlvmError.FieldNotFound.WithMessage($"field '{fa.FieldName}' not found on enum {fqn}").Render();
				return ("undef", new CirType.Any());
			}
			var gep = FreshTemp();
			_bodyLines.Add($"  {gep} = getelementptr inbounds {StructName(fqn)}, ptr {targetVal}, i32 0, i32 {idx}");
			return (gep, fieldTy);
		}

		if (!_classByFqn.ContainsKey(fqn)) {
			LlvmError.FieldNotFound.WithMessage($"cannot resolve owning type of field '{fa.FieldName}'").Render();
			return ("undef", new CirType.Any());
		}

		// Look up the field in the flattened layout (own fields + ancestor contributions)
		// so an ancestor-declared field GEPs to its correct offset in the child's struct.
		var flat = GetFlattenedFields(fqn);
		var idx2 = flat.FindIndex(f => f.Name == fa.FieldName);
		if (idx2 < 0) {
			LlvmError.FieldNotFound.WithMessage($"field '{fa.FieldName}' not found on {fqn}").Render();
			return ("undef", new CirType.Any());
		}

		var gep2 = FreshTemp();
		_bodyLines.Add($"  {gep2} = getelementptr inbounds {StructName(fqn)}, ptr {targetVal}, i32 0, i32 {idx2}");
		return (gep2, flat[idx2].Type);
	}

	private string EmitCall(CirExpr.Call c) {
		// For known externs, use the pre-computed LLVM symbol (literal C name for @Extern,
		// LLVM-mangled form for cross-project externs). For other targets, mangle on the spot.
		var llvmName = _externLlvmName.TryGetValue(c.MangledName, out var ext) ? ext : MangleToLlvm(c.MangledName);
		var isVariadic = _variadicLeading.ContainsKey(c.MangledName);

		// Variadic externs: type each arg from the expression itself (locals via _localTypeMap,
		// literals from their CIR variant). Avoids the "first registered signature wins" bug.
		// Non-variadic: keep the existing behavior — types come from the registered function's signature.
		List<string> argTypes;
		if (isVariadic) {
			argTypes = c.Args.Select(LlvmTypeOf).ToList();
		}
		else {
			var callee = _module.Functions.FirstOrDefault(f => f.MangledName == c.MangledName);
			argTypes = callee != null ? callee.Parameters.Select(p => LlvmType(p.Type)).ToList() : Enumerable.Repeat("ptr", c.Args.Count).ToList();
			while (argTypes.Count < c.Args.Count) argTypes.Add("ptr");
		}

		var argVals = c.Args.Select(EmitExpr).ToList();
		var argList = string.Join(", ", argVals.Select((v, i) => $"{argTypes[i]} {v}"));

		string retTy;
		var registered = _module.Functions.FirstOrDefault(f => f.MangledName == c.MangledName);
		retTy = registered != null ? LlvmType(registered.ReturnType) : "void";

		string fnTypePrefix = "";
		if (isVariadic) {
			var leadCount = _variadicLeading[c.MangledName];
			var leadingTypes = string.Join(", ", argTypes.Take(leadCount));
			fnTypePrefix = leadCount == 0 ? " (...)" : $" ({leadingTypes}, ...)";
		}

		if (retTy == "void") {
			_bodyLines.Add($"  call void{fnTypePrefix} @{llvmName}({argList})");
			return "void";
		}

		var t = FreshTemp();
		_bodyLines.Add($"  {t} = call {retTy}{fnTypePrefix} @{llvmName}({argList})");
		return t;
	}

	// Virtual dispatch through the receiver's `__vtable__` header. Loads the vtable pointer
	// from offset 0 of the receiver, GEPs to the global slot, loads the function pointer,
	// and calls it with the receiver passed as the implicit `this` argument.
	private string EmitVirtualCall(CirExpr.VirtualCall vc) {
		var receiver = EmitExpr(vc.Receiver);
		// Load the vtable pointer from the receiver's header (offset 0).
		var vtablePtr = FreshTemp();
		_bodyLines.Add($"  {vtablePtr} = load ptr, ptr {receiver}, align 8");
		// GEP into the vtable's methods array. The vtable struct shape is `{ ptr, [N x ptr] }`
		// — the leading `ptr` is the parent-class vtable used for inheritance walks; field 1
		// is the interface-method array. Use any size >= SlotId+1 so the GEP is well-typed.
		var slotAddr = FreshTemp();
		var vsize = vc.SlotId + 1;
		_bodyLines.Add($"  {slotAddr} = getelementptr {{ ptr, [{vsize} x ptr] }}, ptr {vtablePtr}, i32 0, i32 1, i32 {vc.SlotId}");
		// Load the function pointer from the slot.
		var fnPtr = FreshTemp();
		_bodyLines.Add($"  {fnPtr} = load ptr, ptr {slotAddr}, align 8");

		// Indirect call. Receiver is the first arg (`this`); user args follow. The function
		// type signature is derived from the VirtualCall's recorded CIR return + param types.
		var argVals = vc.Args.Select(EmitExpr).ToList();
		var paramSigParts = new List<string> { "ptr" };
		paramSigParts.AddRange(vc.ParamTypes.Select(LlvmType));
		var fnType = $"{LlvmType(vc.ReturnType)} ({string.Join(", ", paramSigParts)})";
		var argList = string.Join(", ", new[] { $"ptr {receiver}" }.Concat(argVals.Select((v, i) => $"{LlvmType(vc.ParamTypes[i])} {v}")));
		var retTy = LlvmType(vc.ReturnType);
		if (retTy == "void") {
			_bodyLines.Add($"  call {fnType} {fnPtr}({argList})");
			return "void";
		}

		var t = FreshTemp();
		_bodyLines.Add($"  {t} = call {fnType} {fnPtr}({argList})");
		return t;
	}

	// Emit the runtime parent-chain walk for class identity checks. Loads the receiver's
	// vtable, compares against the target's vtable global, and on mismatch loads the
	// parent slot and loops. Returns `(hitLabel, missLabel)` — labels at which the caller
	// must terminate each branch with the appropriate result handling.
	//
	// Loop shape:
	//   entry:    vt0 = load receiver; br loop
	//   loop:     cur = phi [vt0, entry], [parent, iter]
	//             match = icmp eq cur, target
	//             br match → hit | check_null
	//   check_null: isnull = icmp eq cur, null
	//             br isnull → miss | iter
	//   iter:     parent_addr = gep cur, field 0
	//             parent = load
	//             br loop
	//   hit:      <caller's success branch>
	//   miss:     <caller's failure branch>
	private (string hitLabel, string missLabel) EmitVtableChainSearch(string receiver, string targetVtableSym) {
		var entryLabel = FreshLabel("cast_entry");
		var loopLabel = FreshLabel("cast_loop");
		var iterLabel = FreshLabel("cast_iter");
		var checkNullLabel = FreshLabel("cast_chknull");
		var hitLabel = FreshLabel("cast_hit");
		var missLabel = FreshLabel("cast_miss");

		var initVt = FreshTemp();
		var cur = FreshTemp();
		var match = FreshTemp();
		var isNull = FreshTemp();
		var parentAddr = FreshTemp();
		var parent = FreshTemp();
		var vsize = VtableSlotCount();

		_bodyLines.Add($"  br label %{entryLabel}");
		_bodyLines.Add($"{entryLabel}:");
		_bodyLines.Add($"  {initVt} = load ptr, ptr {receiver}, align 8");
		_bodyLines.Add($"  br label %{loopLabel}");

		_bodyLines.Add($"{loopLabel}:");
		_bodyLines.Add($"  {cur} = phi ptr [{initVt}, %{entryLabel}], [{parent}, %{iterLabel}]");
		_bodyLines.Add($"  {match} = icmp eq ptr {cur}, {targetVtableSym}");
		_bodyLines.Add($"  br i1 {match}, label %{hitLabel}, label %{checkNullLabel}");

		_bodyLines.Add($"{checkNullLabel}:");
		_bodyLines.Add($"  {isNull} = icmp eq ptr {cur}, null");
		_bodyLines.Add($"  br i1 {isNull}, label %{missLabel}, label %{iterLabel}");

		_bodyLines.Add($"{iterLabel}:");
		_bodyLines.Add($"  {parentAddr} = getelementptr {{ ptr, [{vsize} x ptr] }}, ptr {cur}, i32 0, i32 0");
		_bodyLines.Add($"  {parent} = load ptr, ptr {parentAddr}, align 8");
		_bodyLines.Add($"  br label %{loopLabel}");

		return (hitLabel, missLabel);
	}

	// `x as T` / `x as? T` for reference downcasts. The chain-walk loop searches the
	// receiver's class and every ancestor for a vtable matching the target. On match the
	// receiver is the answer; on mismatch `as` aborts and `as?` yields null.
	private string EmitDowncast(CirExpr.Downcast dc) {
		var receiver = EmitExpr(dc.Receiver);
		var (hitLabel, missLabel) = EmitVtableChainSearch(receiver, $"@{MangleVtableGlobal(dc.TargetClassFqn)}");

		if (dc.IsSafe) {
			var doneLabel = FreshLabel("cast_done");
			_bodyLines.Add($"{hitLabel}:");
			_bodyLines.Add($"  br label %{doneLabel}");
			_bodyLines.Add($"{missLabel}:");
			_bodyLines.Add($"  br label %{doneLabel}");
			_bodyLines.Add($"{doneLabel}:");
			_blockTerminated = false;
			var result = FreshTemp();
			_bodyLines.Add($"  {result} = phi ptr [{receiver}, %{hitLabel}], [null, %{missLabel}]");
			return result;
		}

		// Unsafe `as` — abort on miss. The miss block is unreachable after `abort()`, so
		// control only continues from the hit block.
		_bodyLines.Add($"{missLabel}:");
		_bodyLines.Add("  call void @abort()");
		_bodyLines.Add("  unreachable");
		_bodyLines.Add($"{hitLabel}:");
		_blockTerminated = false;
		return receiver;
	}

	// `x is T` — runtime kind check. Walks the same chain as the downcast and returns `i1`
	// (true on match, false on miss). Restricted to class targets; the analyzer rejects
	// other combinations with S01F before reaching here.
	private string EmitTypeCheck(CirExpr.TypeCheck tc) {
		if (tc.TargetType is not CirType.Named { FullyQualifiedName: var targetFqn } || !_classByFqn.ContainsKey(targetFqn)) {
			LlvmError.UnsupportedExpression.WithMessage("`is` target must be a known class").Render();
			return "0";
		}
		var receiver = EmitExpr(tc.Value);
		var (hitLabel, missLabel) = EmitVtableChainSearch(receiver, $"@{MangleVtableGlobal(targetFqn)}");
		var doneLabel = FreshLabel("is_done");
		_bodyLines.Add($"{hitLabel}:");
		_bodyLines.Add($"  br label %{doneLabel}");
		_bodyLines.Add($"{missLabel}:");
		_bodyLines.Add($"  br label %{doneLabel}");
		_bodyLines.Add($"{doneLabel}:");
		_blockTerminated = false;
		var result = FreshTemp();
		_bodyLines.Add($"  {result} = phi i1 [1, %{hitLabel}], [0, %{missLabel}]");
		return result;
	}

	// `x ?? y` — null-coalesce. Yields `x` when non-null, otherwise the value of `y`. Both
	// branches store into a stack slot; mem2reg promotes the alloca/store/load pattern back
	// to a phi during clang's optimization passes. The slot lets us handle right-hand sides
	// that branch internally (nested `??`, ternary, etc.) without tracking the current
	// basic block label by hand. Restricted to pointer-typed nullables for now.
	private string EmitNullCoalesce(CirExpr.NullCoalesce nc) {
		var leftTy = LlvmTypeOf(nc.Left);
		if (leftTy != "ptr") {
			LlvmError.UnsupportedExpression.WithMessage($"`??` on non-pointer nullable type '{leftTy}' is not yet supported").Render();
			return "undef";
		}

		// Stack slot for the coalesced result. Allocated up front so both branches share it.
		var resultAddr = $"%coalesce_result.{_tempCounter++}";
		_allocaLines.Add($"  {resultAddr} = alloca {leftTy}, align 8");

		var leftVal = EmitExpr(nc.Left);
		var rightLabel = FreshLabel("coalesce_right");
		var leftLabel = FreshLabel("coalesce_left");
		var doneLabel = FreshLabel("coalesce_done");

		var isNull = FreshTemp();
		_bodyLines.Add($"  {isNull} = icmp eq {leftTy} {leftVal}, null");
		_bodyLines.Add($"  br i1 {isNull}, label %{rightLabel}, label %{leftLabel}");

		// Right branch: evaluate the fallback and store it into the slot.
		_bodyLines.Add($"{rightLabel}:");
		_blockTerminated = false;
		var rightVal = EmitExpr(nc.Right);
		_bodyLines.Add($"  store {leftTy} {rightVal}, ptr {resultAddr}");
		_bodyLines.Add($"  br label %{doneLabel}");

		// Left branch: store the original (non-null) left value.
		_bodyLines.Add($"{leftLabel}:");
		_blockTerminated = false;
		_bodyLines.Add($"  store {leftTy} {leftVal}, ptr {resultAddr}");
		_bodyLines.Add($"  br label %{doneLabel}");

		// Merge: load whichever was stored.
		_bodyLines.Add($"{doneLabel}:");
		_blockTerminated = false;
		var result = FreshTemp();
		_bodyLines.Add($"  {result} = load {leftTy}, ptr {resultAddr}");
		return result;
	}

	// `cond ? then : else` — evaluate the condition, branch to one side only, store the
	// chosen value into a stack slot, then load through the merge. Mirrors EmitNullCoalesce's
	// shape; mem2reg promotes the alloca/store/load back to a phi at clang's opt pass. The
	// CIR lowering already widened both branches to a common type so the resultTy lookup
	// applies to both `t.Then` and `t.Else`.
	private string EmitTernary(CirExpr.Ternary t) {
		var resultTy = LlvmTypeOf(t.Then);
		var resultAddr = $"%ternary_result.{_tempCounter++}";
		_allocaLines.Add($"  {resultAddr} = alloca {resultTy}, align 8");

		var condVal = EmitExpr(t.Condition);

		var thenLabel = FreshLabel("ternary_then");
		var elseLabel = FreshLabel("ternary_else");
		var doneLabel = FreshLabel("ternary_done");

		_bodyLines.Add($"  br i1 {condVal}, label %{thenLabel}, label %{elseLabel}");

		_bodyLines.Add($"{thenLabel}:");
		_blockTerminated = false;
		var thenVal = EmitExpr(t.Then);
		_bodyLines.Add($"  store {resultTy} {thenVal}, ptr {resultAddr}");
		_bodyLines.Add($"  br label %{doneLabel}");

		_bodyLines.Add($"{elseLabel}:");
		_blockTerminated = false;
		var elseVal = EmitExpr(t.Else);
		_bodyLines.Add($"  store {resultTy} {elseVal}, ptr {resultAddr}");
		_bodyLines.Add($"  br label %{doneLabel}");

		_bodyLines.Add($"{doneLabel}:");
		_blockTerminated = false;
		var loaded = FreshTemp();
		_bodyLines.Add($"  {loaded} = load {resultTy}, ptr {resultAddr}");
		return loaded;
	}

	// `new ClassName(args)` — heap-allocate a zero-initialized instance, run its constructor,
	// return the pointer. Memory model: `calloc(1, sizeof(struct))` guarantees zero bits, so
	// fields without explicit initializers come up as 0/null/false. Lifetime is unmanaged
	// (leaks until explicit `delete`).
	private string EmitAlloc(CirExpr.Alloc a) {
		if (a.Type is not CirType.Named named) {
			LlvmError.UnsupportedExpression.WithMessage($"alloc target must be a named class type, got {a.Type.GetType().Name}").Render();
			return "undef";
		}

		var fqn = named.FullyQualifiedName;
		var structName = StructName(fqn);

		// Compute sizeof(struct) via the GEP-null trick: pointer to element index 1 of a null
		// pointer is the byte offset of one struct, i.e. its size. ptrtoint converts to i64.
		var sizePtr = FreshTemp();
		_bodyLines.Add($"  {sizePtr} = getelementptr {structName}, ptr null, i32 1");
		var sizeI64 = FreshTemp();
		_bodyLines.Add($"  {sizeI64} = ptrtoint ptr {sizePtr} to i64");

		// Heap allocation. calloc zeroes the memory, satisfying the "fields default to zero" rule
		// without an additional memset.
		var obj = FreshTemp();
		_bodyLines.Add($"  {obj} = call ptr @calloc(i64 1, i64 {sizeI64})");

		// Constructor invocation. Receiver (`this`) is the first arg; user-supplied args follow.
		// Argument LLVM types come from the constructor's registered signature when available.
		var ctorLlvm = MangleToLlvm(a.CtorMangledName);
		var argVals = a.Args.Select(EmitExpr).ToList();
		var argTypes = ResolveCallArgTypes(a.CtorMangledName, argVals.Count, skipReceiver: true);
		var argList = string.Join(", ", new[] { $"ptr {obj}" }.Concat(argVals.Select((v, i) => $"{argTypes[i]} {v}")));
		_bodyLines.Add($"  call void @{ctorLlvm}({argList})");

		return obj;
	}

	// Look up an LLVM-typed argument list for a callee, optionally skipping the leading `this`.
	// Falls back to opaque `ptr` when the callee isn't in the module table.
	private List<string> ResolveCallArgTypes(string mangledName, int count, bool skipReceiver) {
		var fn = _module.Functions.FirstOrDefault(f => f.MangledName == mangledName);
		if (fn == null) return Enumerable.Repeat("ptr", count).ToList();
		var src = skipReceiver ? fn.Parameters.Skip(1) : fn.Parameters;
		var types = src.Select(p => LlvmType(p.Type)).ToList();
		while (types.Count < count) types.Add("ptr");
		return types.Take(count).ToList();
	}

	// LLVM type of an expression's runtime value, used when typing variadic arguments
	// and call-site argument lists. Locals/this are looked up in _localTypeMap; literals
	// are typed by their CIR variant; Cast carries an explicit target type.
	private string LlvmTypeOf(CirExpr expr) => expr switch {
		// Integer literals follow the smallest-signed-fit rule used by CIR's TypeInference,
		// keeping LLVM and CIR's type views consistent (e.g. `let x = 5 + 5` is i8 in both layers).
		CirExpr.IntLit i => LlvmType(new CirType.Named(TypeInference.SmallestSignedFit(i.Value))),
		CirExpr.FloatLit => "double",
		CirExpr.BoolLit => "i1",
		CirExpr.CharLit => "i8",
		CirExpr.StrLit => "ptr",
		CirExpr.NullLit => "ptr",
		CirExpr.Local l when _localTypeMap.TryGetValue(l.Name, out var ty) => LlvmType(ty),
		CirExpr.ThisPtr => "ptr",
		CirExpr.Cast cast => LlvmType(cast.TargetType),
		// Field access: walk the chain, look up the named field's type in the parent class's
		// declared field list. Falls back to ptr if any link in the chain can't be resolved.
		CirExpr.FieldAccess fa => ResolveTargetFqn(fa.Target) is { } parentFqn && _classByFqn.TryGetValue(parentFqn, out var parentCls) && parentCls.Fields.FirstOrDefault(f => f.Name == fa.FieldName) is { } fld ? LlvmType(fld.Type) : "ptr",
		// Comparisons produce i1; Pow always widens to i64 (integer ops) or double (any
		// float op) so the helper / libm result isn't truncated. Other arithmetic
		// produces the wider operand's LLVM type.
		CirExpr.Binary b => IsComparisonBinOp(b.Op) ? "i1"
			: b.Op == CirBinOp.Pow
				? (IsIntegerLlvmType(LlvmTypeOf(b.Left)) && IsIntegerLlvmType(LlvmTypeOf(b.Right)) ? "i64" : "double")
				: WiderLlvmType(LlvmTypeOf(b.Left), LlvmTypeOf(b.Right)),
		// Unary Neg/BitNot keep the operand's type; logical Not is always i1.
		CirExpr.Unary u => u.Op == CirUnOp.Not ? "i1" : LlvmTypeOf(u.Operand),
		// Static-field loads carry their declared type on the CIR node.
		CirExpr.StaticFieldRef sr => LlvmType(sr.Type),
		// Enum case references are pointers to the per-case constant global.
		CirExpr.EnumCaseRef => "ptr",
		// Array literals produce a slice value.
		CirExpr.ArrayLit => "{ ptr, i64 }",
		// Indexing into a slice produces its element type.
		CirExpr.Index ix => LlvmElementTypeOf(ix.Target),
		// `arr::LENGTH` is always i64.
		CirExpr.ArrayLength => "i64",
		// `new T[n]` produces a slice value.
		CirExpr.NewArray => "{ ptr, i64 }",
		// Sub-slicing also produces a slice value.
		CirExpr.Subslice => "{ ptr, i64 }",
		// Look up the callee's return type from the module's function table; fall back to ptr
		// for unresolved/unknown calls.
		CirExpr.Call call => _module.Functions.FirstOrDefault(f => f.MangledName == call.MangledName) is { } fn ? LlvmType(fn.ReturnType) : "ptr",
		// Vtable references are opaque pointers at runtime; virtual call type is recorded.
		CirExpr.VtableRef => "ptr",
		CirExpr.VirtualCall vc => LlvmType(vc.ReturnType),
		// Downcast yields a pointer (or null for `as?`); TypeCheck is i1.
		CirExpr.Downcast => "ptr",
		CirExpr.TypeCheck => "i1",
		// Ternary: branches were widened to a common type at CIR lowering, so the result
		// type matches either branch. Use the then-branch for the lookup.
		CirExpr.Ternary tn => LlvmTypeOf(tn.Then),
		// NullCoalesce is restricted to pointer-typed nullables today.
		CirExpr.NullCoalesce => "ptr",
		_ => "ptr"
	};

	private static string WiderLlvmType(string a, string b) =>
		a == b ? a : (BitWidth(a) >= BitWidth(b) ? a : b);

	private static bool IsComparisonBinOp(CirBinOp op) =>
		op is CirBinOp.Eq or CirBinOp.NotEq or CirBinOp.Lt or CirBinOp.LtEq or CirBinOp.Gt or CirBinOp.GtEq or CirBinOp.In;

	// Emit a CIR cast to LLVM IR. Supports lossless integer widening (sext for signed,
	// zext for unsigned), float widening (fpext), and integer↔float conversions (sitofp/fptosi).
	private string EmitCast(CirExpr.Cast cast) {
		var dstTy = LlvmType(cast.TargetType);

		// Integer-typed literals are width-agnostic in LLVM IR. When widening such a literal
		// to any integer type, return the literal value directly — avoids `sext i8 5 to i32`
		// boilerplate that would otherwise pepper the output.
		if (dstTy is "i1" or "i8" or "i16" or "i32" or "i64") {
			if (cast.Value is CirExpr.IntLit i) return i.Value;
			if (cast.Value is CirExpr.BoolLit b) return b.Value ? "1" : "0";
			if (cast.Value is CirExpr.CharLit c) return ((int)c.Value).ToString(CultureInfo.InvariantCulture);
		}

		var srcVal = EmitExpr(cast.Value);
		var srcTy = LlvmTypeOf(cast.Value);

		if (srcTy == dstTy) return srcVal;

		var dstName = (cast.TargetType as CirType.Named)?.FullyQualifiedName ?? "";
		var dstSigned = dstName is "i8" or "i16" or "i32" or "i64" or "int" or "long" or "short";
		var t = FreshTemp();

		if (srcTy is "i1" or "i8" or "i16" or "i32" or "i64" && dstTy is "i8" or "i16" or "i32" or "i64") {
			var op = dstSigned ? "sext" : "zext";
			// Same-width src/dst already short-circuited above. Narrowing is not lossless and
			// shouldn't be reachable from the dispatch path; emit truncate as a fallback.
			if (BitWidth(srcTy) > BitWidth(dstTy))
				_bodyLines.Add($"  {t} = trunc {srcTy} {srcVal} to {dstTy}");
			else
				_bodyLines.Add($"  {t} = {op} {srcTy} {srcVal} to {dstTy}");
			return t;
		}

		if (srcTy == "float" && dstTy == "double") {
			_bodyLines.Add($"  {t} = fpext float {srcVal} to double");
			return t;
		}

		if (srcTy == "double" && dstTy == "float") {
			_bodyLines.Add($"  {t} = fptrunc double {srcVal} to float");
			return t;
		}

		// Integer → float. Default to `sitofp` (signed) since unsigned source tracking isn't
		// fully wired through; revisit when u32/u64 → float casts need unsigned semantics.
		if (srcTy is "i1" or "i8" or "i16" or "i32" or "i64" && dstTy is "float" or "double") {
			_bodyLines.Add($"  {t} = sitofp {srcTy} {srcVal} to {dstTy}");
			return t;
		}

		if (srcTy is "float" or "double" && dstTy is "i8" or "i16" or "i32" or "i64") {
			_bodyLines.Add($"  {t} = fptosi {srcTy} {srcVal} to {dstTy}");
			return t;
		}

		// Catch-all bitcast for unhandled cases — keeps compilation moving and surfaces
		// any miscoercions as obvious LLVM errors rather than mysterious crashes.
		_bodyLines.Add($"  {t} = bitcast {srcTy} {srcVal} to {dstTy}");
		return t;
	}

	private static int BitWidth(string llvmType) => llvmType switch {
		"i1" => 1, "i8" => 8, "i16" => 16, "i32" => 32, "i64" => 64,
		"float" => 32, "double" => 64,
		_ => 0
	};

	private string EmitBinary(CirExpr.Binary b) {
		// Power doesn't fit the simple binop shape — route to a helper or libm.
		if (b.Op == CirBinOp.Pow) return EmitPow(b);
		// Pick the wider operand type for arithmetic so i64/f64 expressions emit the right binop.
		var leftTy = LlvmTypeOf(b.Left);
		var rightTy = LlvmTypeOf(b.Right);
		var opTy = WiderLlvmType(leftTy, rightTy);
		var lhs = EmitExpr(b.Left);
		var rhs = EmitExpr(b.Right);
		// LLVM requires matching types on both sides of a binop. Widen any narrower
		// operand to `opTy` so a mix like `i8 + i64` (e.g. after a Pow widens one side)
		// emits as `i64 + i64`.
		lhs = CoerceTo(lhs, leftTy, opTy);
		rhs = CoerceTo(rhs, rightTy, opTy);
		var t = FreshTemp();
		_bodyLines.Add($"  {t} = {LlvmBinOp(b.Op, opTy)} {opTy} {lhs}, {rhs}");
		return t;
	}

	// True when an LLVM scalar type is integer (i1/i8/i16/i32/i64). False for floats and ptr.
	private static bool IsIntegerLlvmType(string ty) =>
		ty is "i1" or "i8" or "i16" or "i32" or "i64";

	// Lower `base ^ exp` (power). Integer-integer routes through the module helper
	// `@cloth_pow_i64` (exponentiation by squaring) and stays i64. Anything involving
	// a float coerces both sides to double and calls libm `pow`, returning double.
	// The typer matches this rule (Pow result is i64 or f64), so the surrounding store
	// or binop sees the right width and the result isn't truncated.
	private string EmitPow(CirExpr.Binary b) {
		var leftTy = LlvmTypeOf(b.Left);
		var rightTy = LlvmTypeOf(b.Right);
		var lhs = EmitExpr(b.Left);
		var rhs = EmitExpr(b.Right);

		if (IsIntegerLlvmType(leftTy) && IsIntegerLlvmType(rightTy)) {
			_needsIntPowHelper = true;
			var lhs64 = WidenIntTo(lhs, leftTy, "i64");
			var rhs64 = WidenIntTo(rhs, rightTy, "i64");
			var call = FreshTemp();
			_bodyLines.Add($"  {call} = call i64 @cloth_pow_i64(i64 {lhs64}, i64 {rhs64})");
			return call;
		}

		// Float path: lift any integer operand to double, call libm `pow`, return double.
		_needsLibmPow = true;
		var lhsD = ToDouble(lhs, leftTy);
		var rhsD = ToDouble(rhs, rightTy);
		var resD = FreshTemp();
		_bodyLines.Add($"  {resD} = call double @pow(double {lhsD}, double {rhsD})");
		return resD;
	}

	// Coerce an emitted value `val` (currently of `fromTy`) to `toTy`. Used by
	// `EmitBinary` so binops see operands of matching widths, which LLVM requires.
	// Handles integer width changes (sext/trunc), float width changes (fpext/fptrunc),
	// and int↔float crossings (sitofp/fptosi). Same-type pass-through returns the
	// original SSA name.
	private string CoerceTo(string val, string fromTy, string toTy) {
		if (fromTy == toTy) return val;
		if (IsIntegerLlvmType(fromTy) && IsIntegerLlvmType(toTy))
			return BitWidth(toTy) > BitWidth(fromTy) ? WidenIntTo(val, fromTy, toTy) : NarrowIntTo(val, fromTy, toTy);
		if (toTy == "double") return ToDouble(val, fromTy);
		if (toTy == "float") {
			var asD = ToDouble(val, fromTy);
			return CoerceTo(asD, "double", "float");
		}
		if (fromTy is "double" or "float" && IsIntegerLlvmType(toTy)) return FromDouble(val, toTy);
		return val;
	}

	private string WidenIntTo(string val, string fromTy, string toTy) {
		if (fromTy == toTy) return val;
		var t = FreshTemp();
		_bodyLines.Add($"  {t} = sext {fromTy} {val} to {toTy}");
		return t;
	}

	private string NarrowIntTo(string val, string fromTy, string toTy) {
		if (fromTy == toTy) return val;
		var t = FreshTemp();
		_bodyLines.Add($"  {t} = trunc {fromTy} {val} to {toTy}");
		return t;
	}

	private string ToDouble(string val, string fromTy) {
		if (fromTy == "double") return val;
		var t = FreshTemp();
		if (fromTy == "float") _bodyLines.Add($"  {t} = fpext float {val} to double");
		else if (IsIntegerLlvmType(fromTy)) _bodyLines.Add($"  {t} = sitofp {fromTy} {val} to double");
		else _bodyLines.Add($"  {t} = bitcast {fromTy} {val} to double"); // fallback; shouldn't hit
		return t;
	}

	private string FromDouble(string val, string toTy) {
		if (toTy == "double") return val;
		var t = FreshTemp();
		if (toTy == "float") _bodyLines.Add($"  {t} = fptrunc double {val} to float");
		else if (IsIntegerLlvmType(toTy)) _bodyLines.Add($"  {t} = fptosi double {val} to {toTy}");
		else _bodyLines.Add($"  {t} = bitcast double {val} to {toTy}");
		return t;
	}

	private string EmitUnary(CirExpr.Unary u) {
		// Pre/post increment/decrement run on an lvalue: load, compute, store, return the
		// value matching the operator's semantics (post returns the old value, pre returns
		// the new). Operates on integer types only — float `++` is not supported in CIR yet.
		if (u.Op is CirUnOp.PostInc or CirUnOp.PostDec or CirUnOp.PreInc or CirUnOp.PreDec) {
			var addr = EmitAddrOf(u.Operand);
			var (lvalueTy, _) = TypeOfLvalue(u.Operand);
			var llvmTy = LlvmType(lvalueTy);
			var oldVal = FreshTemp();
			_bodyLines.Add($"  {oldVal} = load {llvmTy}, ptr {addr}");
			var newVal = FreshTemp();
			var op = u.Op is CirUnOp.PostInc or CirUnOp.PreInc ? "add" : "sub";
			_bodyLines.Add($"  {newVal} = {op} {llvmTy} {oldVal}, 1");
			_bodyLines.Add($"  store {llvmTy} {newVal}, ptr {addr}");
			return u.Op is CirUnOp.PostInc or CirUnOp.PostDec ? oldVal : newVal;
		}

		// Use the operand's actual LLVM type for Neg / BitNot so the result matches the
		// surrounding arithmetic width — `let x = a + -10;` infers the literal `10` as
		// i8 (smallest fit), and `-10` must come back as i8 too, not a hardcoded i32.
		// Logical Not always produces i1 regardless of operand width.
		var operandTy = LlvmTypeOf(u.Operand);
		var operand = EmitExpr(u.Operand);
		var t = FreshTemp();
		switch (u.Op) {
			case CirUnOp.Neg:
				_bodyLines.Add($"  {t} = sub {operandTy} 0, {operand}");
				return t;
			case CirUnOp.Not:
				_bodyLines.Add($"  {t} = xor i1 {operand}, true");
				return t;
			case CirUnOp.BitNot:
				_bodyLines.Add($"  {t} = xor {operandTy} {operand}, -1");
				return t;
		}

		LlvmError.UnsupportedExpression.WithMessage($"unary operator {u.Op} not yet lowered").Render();
		return "undef";
	}

	// Address-of for an lvalue expression (assignment target).
	private string EmitAddrOf(CirExpr expr) {
		switch (expr) {
			case CirExpr.Local l:
				if (_localAddrMap.TryGetValue(l.Name, out var addr)) return addr;
				LlvmError.UnsupportedExpression.WithMessage($"address-of unknown local '{l.Name}'").Render();
				return "undef";
			case CirExpr.FieldAccess fa: return EmitFieldGep(fa).gep;
			case CirExpr.Index ix: return EmitIndexAddr(ix);
		}

		LlvmError.UnsupportedExpression.WithMessage($"cannot take address of {expr.GetType().Name}").Render();
		return "undef";
	}

	// Compute the address of a slice slot: same bounds-checked GEP shape `EmitIndexLoad`
	// uses, minus the trailing `load`. Returns the slot pointer so callers (e.g.
	// `EmitAssign` for `arr[i] = v`) can store through it.
	private string EmitIndexAddr(CirExpr.Index ix) {
		_needsBoundsPanic = true;

		var slice = EmitExpr(ix.Target);
		var data = FreshTemp();
		_bodyLines.Add($"  {data} = extractvalue {{ ptr, i64 }} {slice}, 0");
		var len = FreshTemp();
		_bodyLines.Add($"  {len} = extractvalue {{ ptr, i64 }} {slice}, 1");

		var idx = EmitExpr(ix.Idx);
		var idxTy = LlvmTypeOf(ix.Idx);
		var idx64 = CoerceTo(idx, idxTy, "i64");

		var neg = FreshTemp();
		_bodyLines.Add($"  {neg} = icmp slt i64 {idx64}, 0");
		var oob = FreshTemp();
		_bodyLines.Add($"  {oob} = icmp sge i64 {idx64}, {len}");
		var bad = FreshTemp();
		_bodyLines.Add($"  {bad} = or i1 {neg}, {oob}");
		var panicLabel = FreshLabel("bounds_panic");
		var okLabel = FreshLabel("bounds_ok");
		_bodyLines.Add($"  br i1 {bad}, label %{panicLabel}, label %{okLabel}");
		_bodyLines.Add($"{panicLabel}:");
		_bodyLines.Add($"  call void @__cloth_panic_bounds(i64 {idx64}, i64 {len})");
		_bodyLines.Add($"  unreachable");
		_bodyLines.Add($"{okLabel}:");
		_blockTerminated = false;

		var eltTy = LlvmElementTypeOf(ix.Target);
		var slot = FreshTemp();
		_bodyLines.Add($"  {slot} = getelementptr {eltTy}, ptr {data}, i64 {idx64}");
		return slot;
	}

	private (CirType type, bool found) TypeOfLvalue(CirExpr expr) {
		switch (expr) {
			case CirExpr.Local l:
				return _localTypeMap.TryGetValue(l.Name, out var ty) ? (ty, true) : (new CirType.Any(), false);
			case CirExpr.FieldAccess fa:
				var fqn = ResolveTargetFqn(fa.Target);
				if (fqn != null && _classByFqn.ContainsKey(fqn)) {
					var f = GetFlattenedFields(fqn).FirstOrDefault(x => x.Name == fa.FieldName);
					if (f != null) return (f.Type, true);
				}
				if (fqn != null && _enumByFqn.TryGetValue(fqn, out var enumDecl)) {
					var (idx, fieldTy) = ResolveEnumFieldIndex(enumDecl, fa.FieldName);
					if (idx >= 0) return (fieldTy, true);
				}

				return (new CirType.Any(), false);
			case CirExpr.Index ix:
				// `arr[i] = v` — the slot's CIR type is the array's element type. We walk
				// the target through the same shapes `LlvmElementTypeOf` knows about so
				// indexing into a local array, a call result, or a field-access array all
				// resolve correctly.
				if (TryGetArrayElementCirType(ix.Target, out var elemTy)) return (elemTy, true);
				return (new CirType.Any(), false);
		}

		return (new CirType.Any(), false);
	}

	// Walk an array-typed CIR expression to recover its element type as a CirType.
	// Used by `TypeOfLvalue` (so `arr[i] = v` knows the slot type for the LLVM store
	// and any coercion of `v`). Routes through `GetCirArrayResultType` so the two
	// element-type views stay in sync across expression shapes.
	private bool TryGetArrayElementCirType(CirExpr target, out CirType element) {
		var arr = GetCirArrayResultType(target);
		if (arr != null) {
			element = arr.Element;
			return true;
		}
		element = new CirType.Any();
		return false;
	}

	private string? ResolveTargetFqn(CirExpr target) => target switch {
		CirExpr.ThisPtr => _currentThisFqn,
		CirExpr.Local l when _localTypeMap.TryGetValue(l.Name, out var ty) => ty is CirType.Ptr p && p.Inner is CirType.Named np ? np.FullyQualifiedName : ty is CirType.Named n ? n.FullyQualifiedName : null,
		// Enum case singletons: the FQN of the value is the enum's FQN. Used by method
		// calls on `EnumName.CASE.someGetter()`.
		CirExpr.EnumCaseRef ec => ec.EnumFqn,
		// Nested field-access chains (e.g. `this.__outer__.outerField`): resolve the inner
		// FieldAccess to its containing class, then look up the named field via the
		// flattened layout (so an ancestor-declared field still resolves).
		CirExpr.FieldAccess fa when ResolveTargetFqn(fa.Target) is { } parentFqn && _classByFqn.ContainsKey(parentFqn) => GetFlattenedFields(parentFqn).FirstOrDefault(f => f.Name == fa.FieldName)?.Type is CirType.Named fn ? fn.FullyQualifiedName : null,
		_ => null
	};

