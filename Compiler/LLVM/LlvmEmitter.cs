using System.Globalization;
using System.Text;
using Compiler.CIR;
using Compiler.Configs;
using Compiler.Semantics;

namespace Compiler.LLVM;

// Lowers a CirModule to LLVM IR text (.ll) and writes it to {projectRoot}/build/{ProjectName}.ll.
// Uses LLVM 15+ opaque-pointer style and the alloca/load/store pattern for all locals;
// `mem2reg` (run by clang) promotes them to SSA.
public sealed class LlvmEmitter {
	private readonly CirModule _module;
	private readonly ClothConfig _config;
	private readonly string _projectRoot;

	private readonly Dictionary<string, CirTypeDecl.Class> _classByFqn = new();
	// Enum FQN → CIR enum decl. Parallel to `_classByFqn`. Lets `EmitFieldGep` /
	// `LlvmTypeOf` resolve a field reference when the receiver is an enum singleton.
	private readonly Dictionary<string, CirTypeDecl.Enum> _enumByFqn = new();
	// classFqn → flattened field layout (root-to-leaf), with the vtable header only
	// contributed by the root. Lazily populated by GetFlattenedFields and used by both
	// struct-type emission and field-GEP indexing so a child instance can read/write
	// ancestor fields at their natural offsets.
	private readonly Dictionary<string, List<CirField>> _flattenedFields = new();
	private readonly HashSet<string> _definedFns = new();
	private readonly Dictionary<string, string> _externDecls = new();

	private readonly List<string> _strings = new();
	private readonly Dictionary<string, int> _stringIndex = new();

	// CirFunctions whose IsExtern == true. Keyed by MangledName (literal C symbol for @Extern,
	// or dotted CIR FQN for cross-project externs). Provides proper signatures for `declare` lines.
	private readonly Dictionary<string, CirFunction> _externFns = new();

	// CIR MangledName → final LLVM symbol. For @Extern (literal C symbol) the value matches the key;
	// for cross-project externs (dotted CIR FQN) the value is the LLVM-mangled form.
	private readonly Dictionary<string, string> _externLlvmName = new();

	// Set by `EmitPow` when at least one `^` operator emits an integer or float code path.
	// Drives the module-level helper / libm extern emission in `Build()`.
	private bool _needsIntPowHelper;
	private bool _needsLibmPow;

	// Set by `EmitIndexLoad` when any `arr[i]` access fires. Drives the lazy emission of
	// the `@__cloth_panic_bounds` helper and the libc `fprintf` / `stderr` declarations
	// used by it.
	private bool _needsBoundsPanic;

	// C-symbol → number of leading fixed parameters for variadic externs.
	// Populated when multiple @Extern declarations alias to the same C symbol with different
	// signatures (e.g. _printf_i32, _printf_i64 both → "printf"); the LLVM declare and call
	// syntax must use variadic form so each call site can pass its own argument types.
	private readonly Dictionary<string, int> _variadicLeading = new();

	// Per-function state
	private int _tempCounter;
	private string _currentThisFqn = "";
	private CirType _currentReturnType = new CirType.Void();
	private readonly List<string> _allocaLines = new();
	private readonly List<string> _prologueLines = new();
	private readonly List<string> _bodyLines = new();
	private bool _blockTerminated;
	private readonly Dictionary<string, string> _localAddrMap = new();
	private readonly Dictionary<string, CirType> _localTypeMap = new();

	// Stack of (continueLabel, breakLabel) per enclosing loop. `break;` jumps to the top
	// frame's break label; `continue;` to its continue label. Pushed at the start of a loop
	// body, popped at end. Empty outside any loop — break/continue outside a loop is a
	// hard error in LLVM (no terminator target), so the analyzer should reject those.
	private readonly Stack<(string Continue, string Break)> _loopStack = new();

	public LlvmEmitter(CirModule module, ClothConfig config, string projectRoot) {
		_module = module;
		_config = config;
		_projectRoot = projectRoot;

		foreach (var t in _module.Types) {
			if (t is CirTypeDecl.Class c)
				_classByFqn[c.FullyQualifiedName] = c;
			else if (t is CirTypeDecl.Enum e)
				_enumByFqn[e.FullyQualifiedName] = e;
		}
	}

	public string Emit() {
		var llText = Build();
		var buildDir = Path.Combine(_projectRoot, "build");
		Directory.CreateDirectory(buildDir);
		var llPath = Path.Combine(buildDir, _config.Project.Name + ".ll");
		File.WriteAllText(llPath, llText, new UTF8Encoding(false));
		return llPath;
	}

	// -------------------------------------------------------------------------
	// Module assembly
	// -------------------------------------------------------------------------

	private string Build() {
		PreScan();

		var sb = new StringBuilder();
		sb.AppendLine("; Generated by the Cloth Compiler");
		sb.AppendLine($"source_filename = \"{_config.Project.Name}\"");
		sb.AppendLine("target datalayout = \"e-m:w-p270:32:32-p271:32:32-p272:64:64-i64:64-i128:128-f80:128-n8:16:32:64-S128\"");
		sb.AppendLine($"target triple = \"{ResolveTriple(_config.Build.Target)}\"");
		sb.AppendLine();

		var types = EmitStructTypes();
		if (types.Length > 0) {
			sb.Append(types);
			sb.AppendLine();
		}

		var strs = EmitStringGlobals();
		if (strs.Length > 0) {
			sb.Append(strs);
			sb.AppendLine();
		}

		var externs = EmitExternDecls();
		if (externs.Length > 0) {
			sb.Append(externs);
			sb.AppendLine();
		}

		var vtables = EmitVtableGlobals();
		if (vtables.Length > 0) {
			sb.Append(vtables);
			sb.AppendLine();
		}

		var statics = EmitStaticFieldGlobals();
		if (statics.Length > 0) {
			sb.Append(statics);
			sb.AppendLine();
		}

		var enums = EmitEnumGlobals();
		if (enums.Length > 0) {
			sb.Append(enums);
			sb.AppendLine();
		}

		var enumFunctions = EmitEnumSyntheticFunctions();
		if (enumFunctions.Length > 0) {
			sb.Append(enumFunctions);
			sb.AppendLine();
		}

		foreach (var fn in _module.Functions) {
			if (fn.IsExtern) continue;
			// Compiler-synthesized enum helpers (e.g. `valueOf(string)`, `values()`) have
			// their LLVM bodies emitted by `EmitEnumSyntheticFunctions` below, not through
			// the normal CIR-to-LLVM path. Skipping here avoids emitting a malformed empty
			// define.
			if (fn.Kind is CirFunctionKind.EnumValueOf or CirFunctionKind.EnumValues) continue;
			sb.Append(EmitFunction(fn));
			sb.AppendLine();
		}

		// Integer pow helper: lazy-emitted exponentiation-by-squaring (set in PreScan when
		// any `^` uses two integer operands). Float pow is declared as an extern up in
		// PreScan and routed to libm at the call site.
		if (_needsIntPowHelper) {
			sb.AppendLine(EmitIntPowHelper());
			sb.AppendLine();
		}

		// Bounds-check panic helper. Called from every out-of-range `arr[i]` site with
		// the (i64 idx, i64 len) pair; prints a diagnostic to stderr and aborts.
		if (_needsBoundsPanic) {
			sb.AppendLine(EmitBoundsPanicHelper());
			sb.AppendLine();
		}

		if (_config.Build.OutputType == OutputType.Executable)
			sb.Append(EmitMainEntry());
		return sb.ToString();
	}

	// Bounds-check panic helper. Each `arr[i]` call site emits a check + branch that
	// calls into this helper on failure. Prints a diagnostic to stdout, flushes the
	// stream (so the message survives `abort`'s stdio drop), then aborts.
	private static string EmitBoundsPanicHelper() => string.Join("\n", new[] {
		"@.cloth_bounds_msg = private unnamed_addr constant [49 x i8] c\"cloth: array index %lld out of bounds [0, %lld)\\0A\\00\", align 1",
		"define void @__cloth_panic_bounds(i64 %idx, i64 %len) {",
		"  call i32 (ptr, ...) @printf(ptr @.cloth_bounds_msg, i64 %idx, i64 %len)",
		"  call i32 @fflush(ptr null)",
		"  call void @abort()",
		"  unreachable",
		"}"
	});

	// Exponentiation-by-squaring on i64. Returns 0 for non-positive exponents so the
	// integer path stays integer-typed; users wanting fractional results (e.g. `5 ^ -1`)
	// must cast the base to float first, routing through the libm path instead.
	private static string EmitIntPowHelper() => string.Join("\n", new[] {
		"define i64 @cloth_pow_i64(i64 %base, i64 %exp) {",
		"entry:",
		"  br label %loop",
		"loop:",
		"  %b = phi i64 [ %base, %entry ], [ %b_sq, %iter ]",
		"  %e = phi i64 [ %exp,  %entry ], [ %e_shr, %iter ]",
		"  %r = phi i64 [ 1,     %entry ], [ %r_next, %iter ]",
		"  %done = icmp sle i64 %e, 0",
		"  br i1 %done, label %exit, label %iter",
		"iter:",
		"  %odd = and i64 %e, 1",
		"  %is_odd = icmp ne i64 %odd, 0",
		"  %r_mul = mul i64 %r, %b",
		"  %r_next = select i1 %is_odd, i64 %r_mul, i64 %r",
		"  %b_sq = mul i64 %b, %b",
		"  %e_shr = ashr i64 %e, 1",
		"  br label %loop",
		"exit:",
		"  ret i64 %r",
		"}"
	});

	private static string ResolveTriple(string target) => target switch {
		"x64_86" or "x86_64" or "" => "x86_64-pc-windows-msvc",
		"x86" => "i686-pc-windows-msvc",
		"arm64" => "aarch64-pc-windows-msvc",
		_ => "x86_64-pc-windows-msvc"
	};

	// -------------------------------------------------------------------------
	// Pre-scan: collect defined functions, string literals, extern call targets
	// -------------------------------------------------------------------------

	private void PreScan() {
		// Group extern functions by C symbol to detect collisions (variadic candidates).
		var externsByName = new Dictionary<string, List<CirFunction>>();
		foreach (var fn in _module.Functions) {
			if (fn.IsExtern) {
				if (!externsByName.TryGetValue(fn.MangledName, out var list))
					externsByName[fn.MangledName] = list = new List<CirFunction>();
				list.Add(fn);
			}
			else {
				_definedFns.Add(fn.MangledName);
			}
		}

		foreach (var (cname, group) in externsByName) {
			var representative = group[0];
			_externFns[cname] = representative;
			// Dotted names are CIR FQNs (cross-project externs) and need LLVM mangling.
			// Names without dots are already final C symbols (from @Extern annotations).
			var llvmName = cname.Contains('.') ? MangleToLlvm(cname) : cname;
			_externLlvmName[cname] = llvmName;
			var retTy = LlvmType(representative.ReturnType);

			if (group.Count == 1) {
				var argSig = string.Join(", ", representative.Parameters.Select(p => LlvmType(p.Type)));
				_externDecls[cname] = $"declare {retTy} @{llvmName}({argSig})";
			}
			else {
				// Multiple @Extern declarations for the same C symbol — emit variadic.
				// Leading fixed parameters are the longest common prefix where all groups agree.
				var leading = LongestCommonParamPrefix(group);
				_variadicLeading[cname] = leading.Count;
				var leadSig = string.Join(", ", leading.Select(LlvmType));
				_externDecls[cname] = leading.Count == 0 ? $"declare {retTy} @{llvmName}(...)" : $"declare {retTy} @{llvmName}({leadSig}, ...)";
			}
		}

		foreach (var fn in _module.Functions) {
			foreach (var stmt in fn.Body)
				ScanStmt(stmt);
		}

		foreach (var t in _module.Types) {
			if (t is CirTypeDecl.Class c) {
				foreach (var f in c.Fields)
					if (f.Initializer != null)
						ScanExpr(f.Initializer);
			}
			// Pre-pool enum case-name strings (consumed by `getOrdinal()` / `name()` /
			// `EmitEnumGlobals`) and any string-literal constructor args so the strings
			// section emits a slot for them before the enum globals reference `@.str.N`.
			else if (t is CirTypeDecl.Enum enumDecl) {
				foreach (var ecase in enumDecl.Cases) {
					PoolStringIndex(ecase.Name);
					foreach (var arg in ecase.ConstructorArgs)
						ScanExpr(arg);
				}
			}
		}

		// libc allocators used by `new`/`delete` lowering. Always declared so the .ll links
		// regardless of whether the user code actually invokes either form.
		_externDecls["calloc"] = "declare ptr @calloc(i64, i64)";
		_externDecls["free"] = "declare void @free(ptr)";
		// Used by failed `as` downcasts to halt the program.
		_externDecls["abort"] = "declare void @abort()";
		// libm `pow` is only declared when `^` operates on at least one float operand
		// (`_needsLibmPow` set by the Pow scan above). Integer `^` uses the in-module
		// helper emitted by `EmitIntPowHelper`.
		if (_needsLibmPow) _externDecls["pow"] = "declare double @pow(double, double)";

		// libc `strcmp` is declared whenever the module defines at least one enum, since
		// the synthesized `valueOf(string)` for every enum uses it for string equality.
		if (_module.Types.Any(t => t is CirTypeDecl.Enum))
			_externDecls["strcmp"] = "declare i32 @strcmp(ptr, ptr)";

		// Bounds-check helper needs `printf` to write a panic message and `fflush` to
		// drain stdout before aborting (printf's line buffer is normally dropped on
		// abort). Both are declared when any `arr[i]` access is present
		// (`_needsBoundsPanic` set during the pre-scan). `abort` is already in the
		// always-declared set above.
		if (_needsBoundsPanic) {
			if (!_externDecls.ContainsKey("printf"))
				_externDecls["printf"] = "declare i32 @printf(ptr, ...)";
			if (!_externDecls.ContainsKey("fflush"))
				_externDecls["fflush"] = "declare i32 @fflush(ptr)";
		}
	}

	private void ScanStmt(CirStmt stmt) {
		switch (stmt) {
			case CirStmt.LocalDecl d:
				if (d.Init != null) ScanExpr(d.Init);
				break;
			case CirStmt.TupleDecl t:
				ScanExpr(t.Init);
				break;
			case CirStmt.Assign a:
				ScanExpr(a.Target);
				ScanExpr(a.Value);
				break;
			case CirStmt.Expr e: ScanExpr(e.Expression); break;
			case CirStmt.Discard d: ScanExpr(d.Expression); break;
			case CirStmt.Return r:
				if (r.Value != null) ScanExpr(r.Value);
				break;
			case CirStmt.If i:
				ScanExpr(i.Condition);
				foreach (var s in i.Then) ScanStmt(s);
				foreach (var (c, b) in i.ElseIfs) {
					ScanExpr(c);
					foreach (var s in b) ScanStmt(s);
				}

				if (i.Else != null)
					foreach (var s in i.Else)
						ScanStmt(s);
				break;
			case CirStmt.While w:
				ScanExpr(w.Condition);
				foreach (var s in w.Body) ScanStmt(s);
				break;
			case CirStmt.DoWhile dw:
				foreach (var s in dw.Body) ScanStmt(s);
				ScanExpr(dw.Condition);
				break;
			case CirStmt.For f:
				ScanStmt(f.Init);
				ScanExpr(f.Condition);
				ScanExpr(f.Iterator);
				foreach (var s in f.Body) ScanStmt(s);
				break;
			case CirStmt.ForIn fi:
				ScanExpr(fi.Iterable);
				foreach (var s in fi.Body) ScanStmt(s);
				break;
			case CirStmt.Switch sw:
				ScanExpr(sw.Subject);
				foreach (var c in sw.Cases) {
					if (c.Pattern != null) ScanExpr(c.Pattern);
					foreach (var s in c.Body) ScanStmt(s);
				}

				break;
			case CirStmt.Throw t: ScanExpr(t.Expression); break;
			case CirStmt.Delete d: ScanExpr(d.Expression); break;
			case CirStmt.Block b:
				foreach (var s in b.Body) ScanStmt(s);
				break;
		}
	}

	private void ScanExpr(CirExpr expr) {
		switch (expr) {
			case CirExpr.StrLit s:
				if (!_stringIndex.ContainsKey(s.Value)) {
					_stringIndex[s.Value] = _strings.Count;
					_strings.Add(s.Value);
				}

				break;
			case CirExpr.Binary b:
				// Detect Pow operands' widths now so the helper / libm extern lands in the
				// right module sections (externs are emitted before function bodies).
				if (b.Op == CirBinOp.Pow) {
					var lt = LlvmTypeOf(b.Left);
					var rt = LlvmTypeOf(b.Right);
					if (IsIntegerLlvmType(lt) && IsIntegerLlvmType(rt)) _needsIntPowHelper = true;
					else _needsLibmPow = true;
				}
				ScanExpr(b.Left);
				ScanExpr(b.Right);
				break;
			case CirExpr.Unary u: ScanExpr(u.Operand); break;
			case CirExpr.FieldAccess fa: ScanExpr(fa.Target); break;
			case CirExpr.Index i:
				// Every `arr[i]` access fires the bounds-panic helper at emission. Set the
				// flag here so PreScan can declare `printf` before externs are emitted.
				_needsBoundsPanic = true;
				ScanExpr(i.Target);
				ScanExpr(i.Idx);
				break;
			case CirExpr.Call c:
				if (!_definedFns.Contains(c.MangledName) && !_externFns.ContainsKey(c.MangledName))
					RecordExternCall(c.MangledName, c.Args.Count);
				foreach (var a in c.Args) ScanExpr(a);
				break;
			case CirExpr.IndirectCall ic:
				ScanExpr(ic.Callee);
				foreach (var a in ic.Args) ScanExpr(a);
				break;
			case CirExpr.VirtualCall vc:
				ScanExpr(vc.Receiver);
				foreach (var a in vc.Args) ScanExpr(a);
				break;
			case CirExpr.VtableRef:
				break;
			case CirExpr.Downcast dc:
				ScanExpr(dc.Receiver);
				break;
			case CirExpr.Alloc a:
				if (!_definedFns.Contains(a.CtorMangledName) && !_externFns.ContainsKey(a.CtorMangledName))
					RecordExternCall(a.CtorMangledName, a.Args.Count + 1);
				foreach (var x in a.Args) ScanExpr(x);
				break;
			case CirExpr.Cast c: ScanExpr(c.Value); break;
			case CirExpr.TypeCheck t: ScanExpr(t.Value); break;
			case CirExpr.Ternary t:
				ScanExpr(t.Condition);
				ScanExpr(t.Then);
				ScanExpr(t.Else);
				break;
			case CirExpr.NullCoalesce n:
				ScanExpr(n.Left);
				ScanExpr(n.Right);
				break;
			case CirExpr.TupleLit t:
				foreach (var e in t.Elements) ScanExpr(e);
				break;
			case CirExpr.Range r:
				ScanExpr(r.Start);
				ScanExpr(r.End);
				break;
			case CirExpr.ArrayLit al:
				foreach (var e in al.Elements) ScanExpr(e);
				break;
			case CirExpr.NewArray na:
				foreach (var s in na.Sizes) ScanExpr(s);
				break;
			case CirExpr.Subslice ss:
				ScanExpr(ss.Target);
				ScanExpr(ss.Lo);
				ScanExpr(ss.Hi);
				break;
			case CirExpr.ArrayLength alen:
				ScanExpr(alen.Target);
				break;
		}
	}

	private static List<CirType> LongestCommonParamPrefix(List<CirFunction> fns) {
		var first = fns[0].Parameters;
		var common = new List<CirType>();
		for (var i = 0; i < first.Count; i++) {
			var ty = first[i].Type;
			if (fns.All(f => i < f.Parameters.Count && f.Parameters[i].Type == ty))
				common.Add(ty);
			else
				break;
		}

		return common;
	}

	private void RecordExternCall(string cirName, int argCount) {
		var llvmName = MangleToLlvm(cirName);
		if (_externDecls.ContainsKey(llvmName)) return;
		var args = string.Join(", ", Enumerable.Repeat("ptr", argCount));
		_externDecls[llvmName] = $"declare void @{llvmName}({args})";
	}

