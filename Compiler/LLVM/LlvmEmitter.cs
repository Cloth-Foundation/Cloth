	private string InternString(string value) {
		// Already collected during pre-scan.
		var idx = _stringIndex[value];
		var (_, byteCount) = EncodeStringConstant(value);
		var t = FreshTemp();
		_bodyLines.Add($"  {t} = getelementptr inbounds [{byteCount} x i8], ptr @.str.{idx}, i32 0, i32 0");
		return t;
	}

	private static (string encoded, int byteCount) EncodeStringConstant(string value) {
		var bytes = Encoding.UTF8.GetBytes(value);
		var sb = new StringBuilder(bytes.Length + 1);
		foreach (var b in bytes) {
			if (b == '\\' || b == '"' || b < 0x20 || b > 0x7e)
				sb.Append('\\').Append(b.ToString("X2"));
			else
				sb.Append((char)b);
		}

		sb.Append("\\00");
		return (sb.ToString(), bytes.Length + 1);
	}

