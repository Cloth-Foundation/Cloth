	DotDotDot,
	DotDot,
	ColonColon,
	ReturnArrow,
	Arrow,
	Fallback,
	PlusPlus,
	MinusMinus,
	PlusEqual,
	MinusEqual,
	StarEqual,
	SlashEqual,
	PercentEqual,
	AndEqual,
	OrEqual,
	CaretEqual,
	EqualEqual,
	NotEqual,
	LessEqual,
	GreaterEqual,
	Equal,
	Less,
	Greater,
	Plus,
	Minus,
	Star,
	Slash,
	Percent,
	And,
	Or,
	Caret,
	Tilde,
	Not,
	Dot,
	Comma,
	Semicolon,
	Colon,
	LParen,
	RParen,
	LBrace,
	RBrace,
	LBracket,
	RBracket,
	At,
	Hash,
	Dollar,
	Question,
	Backtick
